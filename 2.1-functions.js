function describeCountry(country, population, capitalCity) {

    return country+' '+'has '+population+ ' people and its capital city is '+ capitalCity;
}
console.log(describeCountry('Italy', 60000000, 'Rome'));
console.log(describeCountry('China', '1.4 Billion', 'Beijing'));
console.log(describeCountry('Mexico', '129 million', 'Mexico city'));

